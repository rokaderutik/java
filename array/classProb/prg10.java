class arrayDemo {

	public static void main(String[] Args) {
	
		int arr1[] = {10, 20};
		char arr2[] = {'A', 'B'};
		float arr3[] = {10.5f, 20.5f};
		double arr4[] = {10.00, 20.00};
		boolean arr5[] = {true, false};

		System.out.println("int array:  " + arr1);
		System.out.println("char array:  " + arr2);
		System.out.println("float array:  " + arr3);
		System.out.println("double array:  " + arr4);
		System.out.println("boolean array:  " + arr5);
	}	
}
